/**
 * @file constants.h
 * @brief Define constants used in the program.
 *
 * This header file defines various constants that are used in the program. These constants
 * include return codes that indicate the status of operations in the program.
 */

#ifndef CONSTANTS_H_
#define CONSTANTS_H_

/**
 * @brief The operation was successful.
 */
#define OK 0

/**
 * @brief An error related to matrix size.
 */
#define SIZE_ERROR -1

/**
 * @brief An error related to matrix elements.
 */
#define ELEMENTS_ERROR -2

/**
 * @brief An error related to memory allocation.
 */
#define MEMORY_ERROR -3

/**
 * @brief An error related to power or exponentiation.
 */
#define POWER_ERROR -4

#endif
